#!/usr/bin/env node
import fs from "node:fs";
import path from "node:path";

const ACTIONS = [
  "copy_candidate",
  "review_required",
  "protect_project_owned",
  "ignore"
];

function usage(exitCode = 0) {
  const text = `GameSpec extraction planner

Usage:
  node bin/gamespec-plan-extraction.js --report <classification.json> [--out <path>] [--format markdown|json]

Rules:
  - Reads a classification report generated by gamespec-classify.
  - Produces a copy/protect/review plan.
  - Never copies files.
  - Refuses to write the plan inside the classified project.
`;
  (exitCode === 0 ? console.log : console.error)(text);
  process.exit(exitCode);
}

function parseArgs(argv) {
  const args = { format: "markdown" };
  for (let i = 0; i < argv.length; i += 1) {
    const arg = argv[i];
    if (arg === "--help" || arg === "-h") usage(0);
    if (arg === "--report") {
      args.report = argv[++i];
    } else if (arg === "--out") {
      args.out = argv[++i];
    } else if (arg === "--format") {
      args.format = argv[++i];
    } else {
      console.error(`Unknown argument: ${arg}`);
      usage(1);
    }
  }
  if (!args.report) {
    console.error("Missing --report <classification.json>.");
    usage(1);
  }
  if (!["markdown", "json"].includes(args.format)) {
    throw new Error(`Unsupported --format: ${args.format}`);
  }
  return args;
}

function normalizePath(inputPath) {
  return path.resolve(inputPath);
}

function isInside(child, parent) {
  const rel = path.relative(parent, child);
  return rel === "" || (!!rel && !rel.startsWith("..") && !path.isAbsolute(rel));
}

function posixJoin(...parts) {
  return parts.filter(Boolean).join("/").replaceAll(/\/+/g, "/");
}

function stripPrefix(value, prefix) {
  return value.startsWith(prefix) ? value.slice(prefix.length) : value;
}

function runtimeTarget(sourcePath) {
  if (sourcePath.startsWith(".claude/")) {
    return posixJoin("runtime/claude", stripPrefix(sourcePath, ".claude/"));
  }
  if (sourcePath.startsWith(".codex/")) {
    return posixJoin("runtime/codex", stripPrefix(sourcePath, ".codex/"));
  }
  if (sourcePath.startsWith(".agents/")) {
    return posixJoin("runtime/agents", stripPrefix(sourcePath, ".agents/"));
  }
  if (sourcePath.startsWith(".opencode/")) {
    return posixJoin("runtime/opencode", stripPrefix(sourcePath, ".opencode/"));
  }
  return posixJoin("runtime/unclassified", sourcePath);
}

function kernelTarget(sourcePath) {
  const base = path.posix.basename(sourcePath);
  if (sourcePath.startsWith("gamespec/agents/")) {
    return posixJoin("kernel/roles/agents", stripPrefix(sourcePath, "gamespec/agents/"));
  }
  if (sourcePath.startsWith("gamespec/skills/")) {
    return posixJoin("kernel/skills", stripPrefix(sourcePath, "gamespec/skills/"));
  }
  if (sourcePath.startsWith("gamespec/templates/")) {
    return posixJoin("kernel/templates", stripPrefix(sourcePath, "gamespec/templates/"));
  }
  if (sourcePath.startsWith("gamespec/workflows/")) {
    return posixJoin("kernel/workflows", stripPrefix(sourcePath, "gamespec/workflows/"));
  }
  if (sourcePath === "gamespec/AGENTS.md") {
    return "kernel/method/AGENTS.md";
  }
  if (sourcePath === "gamespec/config.yaml") {
    return "kernel/config.yaml";
  }
  if (sourcePath === "gamespec/README.md") {
    return "docs/source-audit/project-gamespec-readme.md";
  }
  if (sourcePath === "gamespec/ROADMAP.md") {
    return "docs/source-audit/project-roadmap.md";
  }
  if (/^P8-.*\.md$/i.test(base)) {
    return posixJoin("packs/ue5-data-authoring/protocols", base);
  }
  if (/^P[0-9]-.*\.md$/i.test(base)) {
    return posixJoin("kernel/protocols", base);
  }
  return posixJoin("kernel/unclassified", stripPrefix(sourcePath, "gamespec/"));
}

function planForFile(file) {
  const sourcePath = file.path;
  switch (file.label) {
    case "runtime_candidate":
      return {
        action: "copy_candidate",
        sourcePath,
        proposedTarget: runtimeTarget(sourcePath),
        confidence: "medium",
        reason: "Runtime adapter candidate. Copy only after host adapter shape is defined."
      };
    case "kernel_candidate": {
      const proposedTarget = kernelTarget(sourcePath);
      const needsReview = proposedTarget.includes("unclassified") ||
        proposedTarget.includes("source-audit") ||
        proposedTarget.includes("packs/");
      return {
        action: needsReview ? "review_required" : "copy_candidate",
        sourcePath,
        proposedTarget,
        confidence: needsReview ? "low" : "medium",
        reason: needsReview
          ? "Candidate needs product judgment before copy; may be protocol, pack, or source evidence."
          : "Reusable method asset candidate."
      };
    }
    case "overlay_candidate":
      return {
        action: "protect_project_owned",
        sourcePath,
        proposedTarget: null,
        confidence: "high",
        reason: "Project-bound judging principle. Treat as overlay candidate, not reusable kernel by default."
      };
    case "project_truth":
      return {
        action: "protect_project_owned",
        sourcePath,
        proposedTarget: null,
        confidence: "high",
        reason: "Actual game truth. Never overwrite or extract automatically."
      };
    case "derived_or_archive":
      return {
        action: "protect_project_owned",
        sourcePath,
        proposedTarget: null,
        confidence: "high",
        reason: "Review, archive, or derived material. Keep as project-owned evidence."
      };
    default:
      return {
        action: "ignore",
        sourcePath,
        proposedTarget: null,
        confidence: "medium",
        reason: "Outside current GameSpec package scope."
      };
  }
}

function countByAction(items) {
  const counts = Object.fromEntries(ACTIONS.map((action) => [action, 0]));
  for (const item of items) counts[item.action] += 1;
  return counts;
}

function firstItems(items, action, limit = 16) {
  return items.filter((item) => item.action === action).slice(0, limit);
}

function renderMarkdown(plan) {
  const lines = [];
  lines.push("# GameSpec Kernel Extraction Plan");
  lines.push("");
  lines.push(`Project: \`${plan.projectRoot}\``);
  lines.push(`Generated: ${plan.generated}`);
  lines.push("");
  lines.push("## Summary");
  lines.push("");
  lines.push("| Action | Count |");
  lines.push("| --- | ---: |");
  for (const action of ACTIONS) {
    lines.push(`| \`${action}\` | ${plan.counts[action]} |`);
  }
  lines.push("");
  lines.push("## Safety Invariants");
  lines.push("");
  for (const invariant of plan.safetyInvariants) {
    lines.push(`- ${invariant}`);
  }
  lines.push("");

  for (const action of ACTIONS) {
    lines.push(`## ${action}`);
    lines.push("");
    const items = firstItems(plan.items, action);
    if (items.length === 0) {
      lines.push("_No items._");
    } else {
      for (const item of items) {
        const target = item.proposedTarget ? ` -> \`${item.proposedTarget}\`` : "";
        lines.push(`- \`${item.sourcePath}\`${target}`);
        lines.push(`  - confidence: ${item.confidence}`);
        lines.push(`  - reason: ${item.reason}`);
      }
      const remaining = plan.counts[action] - items.length;
      if (remaining > 0) {
        lines.push(`- ... ${remaining} more`);
      }
    }
    lines.push("");
  }

  lines.push("## Next Step");
  lines.push("");
  lines.push("Review `review_required` items before copying anything. The next implementation slice should turn accepted copy candidates into a dry-run copy manifest, not perform the copy yet.");
  lines.push("");
  return `${lines.join("\n")}\n`;
}

function renderJson(plan) {
  return `${JSON.stringify(plan, null, 2)}\n`;
}

function main() {
  const args = parseArgs(process.argv.slice(2));
  const reportPath = normalizePath(args.report);
  const report = JSON.parse(fs.readFileSync(reportPath, "utf8"));
  if (!Array.isArray(report.files) || !report.projectRoot) {
    throw new Error("Classification report must contain projectRoot and files.");
  }

  let outPath = null;
  if (args.out) {
    outPath = normalizePath(args.out);
    if (isInside(outPath, normalizePath(report.projectRoot))) {
      throw new Error(`Refusing to write plan inside classified project: ${outPath}`);
    }
  }

  const items = report.files.map(planForFile);
  const plan = {
    projectRoot: report.projectRoot,
    sourceReport: reportPath,
    generated: new Date().toISOString(),
    counts: countByAction(items),
    safetyInvariants: [
      "Do not copy, overwrite, stage, or rewrite project truth.",
      "Overlay candidates require an explicit project/product decision before reuse.",
      "Review and archive material remains project-owned evidence.",
      "Runtime candidates require host adapter design before install.",
      "This plan is advisory and performs no file copy."
    ],
    items
  };

  const rendered = args.format === "json" ? renderJson(plan) : renderMarkdown(plan);
  if (outPath) {
    fs.mkdirSync(path.dirname(outPath), { recursive: true });
    fs.writeFileSync(outPath, rendered, "utf8");
  } else {
    process.stdout.write(rendered);
  }
}

try {
  main();
} catch (error) {
  console.error(`gamespec-plan-extraction: ${error.message}`);
  process.exit(1);
}
